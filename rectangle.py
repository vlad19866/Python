# Модуль rectangle содержит функции, которые вьшолняют
# вычисления, связанные с прямоугольниками.

# функция area принимает ширину и длину прямоугольника
# в качесмтве аргументов и возвращает площадь прямоугольника
def area(wight, length):
    return wight * length

# функция perimeter принимает ширину и длину прямоугольника
# в качестве аргументо и возвращает периметр прямоугольника
def perimeter(wight, length):
    return 2 * (wight + length)


